// Arithmetic operations
let a = 10, b = 20;
console.log(a + b, a - b, a * b, a / b, a % b);

// If-Else statement
if (a > b) {
  console.log("a is greater than b");
} else {
  console.log("a is not greater than b");
}

// Switch statement
switch (a) {
  case 10:
    console.log("a is 10");
    break;
  default:
    console.log("a is not 10");
}

// For loop
for (let i = 0; i < 5; i++) {
  console.log(i);
}

// While loop
let j = 0;
while (j < 5) {
  console.log(j);
  j++;
}

// Do-While loop
let k = 0;
do {
  console.log(k);
  k++;
} while (k < 5);
